    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       if(!txtUsuario.getText().isEmpty()){
            if(UsuarioLogic.eliminar(txtUsuario.getText())){
                JOptionPane.showMessageDialog(this, "Usuario Eliminado Satisfactoriamente");
            }else{
                JOptionPane.showMessageDialog(this,"Usuario no encontrado");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(!txtNombre.getText().isEmpty()&&
                !txtApellidos.getText().isEmpty()&&
                !txtCorreo.getText().isEmpty()&&
                !txtUsuario.getText().isEmpty()&&
                !txtPassword.getText().isEmpty()&&
                !txtRepetirPassword.getText().equals(txtPassword.getText())){
            Usuario usuario=new Usuario(txtUsuario.getText(), txtPassword.getText(), txtNombre.getText(), txtApellidos.getText(), txtCorreo.getText());
            if(UsuarioLogic.modificar(usuario)){
                JOptionPane.showMessageDialog(this, "Usuario Modificado Satisfactoriamente");
            }else{
                JOptionPane.showMessageDialog(this,"Usuario no encontrado");
            }
        }else{
          JOptionPane.showMessageDialog(this, "Todos los datos deben estar completos");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

