    private void mostrarContraseñaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarContraseñaActionPerformed
        // TODO add your handling code here:
        if(mostrarContraseña.isSelected()){
            txtPassword.setEchoChar((char)0);
            txtRepetirPassword.setEchoChar((char)0);
        }else{
            txtPassword.setEchoChar('*');
            txtRepetirPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_mostrarContraseñaActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

            Usuario usuario=new Usuario(txtUsuario.getText(), txtPassword.getText(), txtNombre.getText(), txtApellidos.getText(), txtCorreo.getText());
            if(UsuarioLogic.insertar(usuario)){
                JOptionPane.showMessageDialog(this, "Usuario Registrado Satisfactoriamente");
                this.dispose();
                PagePrincipal paginaPrincipal=new PagePrincipal(UsuarioLogic.obtener(txtUsuario.getText()));
                paginaPrincipal.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Usuario ya existente");
            }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        if(!txtNombre.getText().isEmpty()&&
                !txtApellidos.getText().isEmpty()&&
                !txtCorreo.getText().isEmpty()&&
                !txtUsuario.getText().isEmpty()&&
                !txtPassword.getText().isEmpty()&&
                !txtRepetirPassword.getText().equals(txtPassword.getText())){
            Usuario usuario=new Usuario(txtUsuario.getText(), txtPassword.getText(), txtNombre.getText(), txtApellidos.getText(), txtCorreo.getText());
            if(UsuarioLogic.insertar(usuario)){
                JOptionPane.showMessageDialog(this, "Usuario Registrado Satisfactoriamente");
                this.dispose();
                PagePrincipal paginaPrincipal=new PagePrincipal(UsuarioLogic.obtener(txtUsuario.getText()));
                paginaPrincipal.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Usuario ya existente");
            }
        }
    }//GEN-LAST:event_btnRegistarActionPerformed

